<p>
Dr. Kilonzo has done few art research projects and published academic and non-academic articles on his career. He has participated and organized local and international art exhibitions, workshops, seminars, conferences, academic visits and tours inside and outside the country. Dr. Kilonzo is also a full time lecturer at the University of Dar es Salaam, teaching visual art courses.</p>
		
                               
