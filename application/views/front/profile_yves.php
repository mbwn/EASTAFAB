                       <h3>Dr. Yves Goscinny</h3>
                       <p>International Liaision</p>
				    
				       <p>
				           Yves Goscinny is a Belgian self-taught artist, born in Africa (In Léopoldville, now Kinshasa, Democratic Republic of Congo - DRC), who was raised in Spain and then completed his education (Secondary School and University) in Brussels, Belgium. Graduating from university in 1971, he went on to work in developing countries (Honduras, Togo, Haiti, Jamaica, Chad, Ethiopia, Mauritania and Tanzania) for International Organizations (ONU, EU).  </p>
				           
				           <p>With an early interest in art, he had then the opportunity to dedicate collecting the contemporary art existing in the countries he had been assigned to, and with years of mingling with the artists themselves and realizing the difficulties for some of them to get proper exposure, he became more involved by organizing exhibitions for individual artists or for group of artists. In 1997, he himself crossed the border and started to paint to eventually have his first exhibition in 1998 together with a well-known Tanzanian artist. « YG », as he signs his paintings, soon opened his own gallery in East Africa (2002) and became in 2003 the co-founder and Executive Director of the East African Art Biennale - EASTAFAB in Tanzania.</p>
				           
				      
                       
