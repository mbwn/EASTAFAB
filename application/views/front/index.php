<p style="text-align:justify;">The Association holds a Biennale at any place of its choice. But since its inception all the Biennales have been held in Dar es Salaam. A Biennale is an exhibition of artworks submitted by local as well as overseas artists held after every two years. The local Organizing Committees in the East African region are responsible for the selection of the artworks to be exhibited. They usually have their own criteria that guide their selection. Artists are supplied with application forms after every two years upon request.</p>

<p style="text-align:justify;">Since its establishment in 2003, the EASTAFAB has depended chiefly on donors for its activities. All donor support is acknowledged in the Biennale catalogue by printing their official logos on a special page of the catalogue. All participating artists get free copies of the catalogue and the rest are sold to the audience. Donors normally get free copies according to their needs.</p>

<p style="text-align:justify;">We welcome all visual artists in East Africa to form their own Organizing Committees and contact the Dar es Salaam Organizing Committee to send them application forms which have instructions on how to submit artworks during the next Biennale to be held on November 2<sup>nd</sup> to 22<sup>nd</sup>, 2017 in Dar es Salaam.</p>

